"""
Multi-step анализ по Мета-Методу без использования фото
Цепочка из 5 специализированных GPT-запросов для глубокого анализа
"""

from openai import OpenAI
from config import OPENAI_API_KEY, OPENAI_MODEL


class MetaMethodAnalyzer:
    def __init__(self):
        self.client = OpenAI(api_key=OPENAI_API_KEY)
        self.model = OPENAI_MODEL

    def analyze(self, request_text: str, username: str) -> tuple:
        """
        Выполняет полный анализ через 5 шагов
        Возвращает (полный_текст_анализа, общее_количество_токенов)
        """
        total_tokens = 0

        # Шаг 1: Глубинный анализ запроса
        step1_result, tokens1 = self._step1_deep_analysis(request_text, username)
        total_tokens += tokens1

        # Шаг 2: Родовые и кармические паттерны
        step2_result, tokens2 = self._step2_ancestral_patterns(request_text, step1_result)
        total_tokens += tokens2

        # Шаг 3: Энергетика чакр и блоки
        step3_result, tokens3 = self._step3_chakra_analysis(request_text, step1_result)
        total_tokens += tokens3

        # Шаг 4: Трансформационные фразы
        step4_result, tokens4 = self._step4_transformation_phrases(request_text, step1_result, step2_result)
        total_tokens += tokens4

        # Шаг 5: Финальная компоновка
        final_result, tokens5 = self._step5_final_composition(
            username, request_text, step1_result, step2_result, step3_result, step4_result
        )
        total_tokens += tokens5

        return final_result, total_tokens

    def _step1_deep_analysis(self, request_text: str, username: str) -> tuple:
        """Шаг 1: Глубинный анализ запроса и выявление ключевых программ"""

        prompt = f"""Ты — эксперт Мета-Метода. Проанализируй запрос человека для духовной практики самопознания.

Запрос: "{request_text}"

Твоя задача:
1. Определи КЛЮЧЕВУЮ ТЕМУ запроса (финансы/отношения/здоровье/реализация/выгорание)
2. Выяви 3-5 ГЛАВНЫХ ПРОГРАММ, которые мешают движению (формулируй как убеждения в кавычках)
3. Определи ГЛАВНЫЕ УРОКИ ДУШИ - чему человек учится через этот опыт
4. Укажи ЧТО ВАЖНО ИЗМЕНИТЬ для трансформации (3-5 конкретных направлений)

Пиши конкретно, без воды. Используй терапевтичный стиль с эзотерической интонацией.

Формат ответа:
ТЕМА: [одним словом - финансы/отношения/здоровье/реализация]

КЛЮЧЕВЫЕ ПРОГРАММЫ:
- "программа 1"
- "программа 2"
- "программа 3"

УРОКИ ДУШИ:
[2-3 предложения о том, чему душа учится]

ЧТО ВАЖНО ИЗМЕНИТЬ:
- направление 1
- направление 2
- направление 3
"""

        response = self.client.chat.completions.create(
            model=self.model,
            messages=[{"role": "user", "content": prompt}],
            temperature=0.7,
            max_tokens=800
        )

        return response.choices[0].message.content, response.usage.total_tokens

    def _step2_ancestral_patterns(self, request_text: str, step1_result: str) -> tuple:
        """Шаг 2: Родовые влияния и кармические паттерны"""

        prompt = f"""Ты — эксперт Мета-Метода. На основе запроса и первичного анализа, определи родовые и кармические паттерны.

Запрос: "{request_text}"

Первичный анализ:
{step1_result}

Твоя задача:
1. РОДОВЫЕ ВЛИЯНИЯ - что шло по роду (по женской/мужской линии), какие программы передавались
2. ПРОШЛЫЕ ЖИЗНИ - если есть сильный кармический отпечаток (клятвы, обеты, незавершённые опыты)
3. КОНТРАКТЫ И ПОДКЛЮЧКИ - энергетические зацепки, соглашения на страдание/жертвенность

Пиши конкретно, используя язык Мета-Метода.

Формат ответа:
РОДОВЫЕ ВЛИЯНИЯ:
[3-4 предложения конкретно]

ПРОШЛЫЕ ЖИЗНИ:
[2-3 предложения если есть отголоски]

КОНТРАКТЫ И ПОДКЛЮЧКИ:
[2-3 предложения о зацепках]
"""

        response = self.client.chat.completions.create(
            model=self.model,
            messages=[{"role": "user", "content": prompt}],
            temperature=0.7,
            max_tokens=700
        )

        return response.choices[0].message.content, response.usage.total_tokens

    def _step3_chakra_analysis(self, request_text: str, step1_result: str) -> tuple:
        """Шаг 3: Анализ энергетики по чакрам"""

        prompt = f"""Ты — эксперт Мета-Метода. Проанализируй поток энергии по чакрам в контексте запроса.

Запрос: "{request_text}"

Первичный анализ:
{step1_result}

Пройдись по каждой чакре и укажи:
- Состояние потока (сильный/средний/слабый/заблокирован)
- Как это связано с запросом

Формат ответа (используй эмодзи для чакр):

🔴 Чакра 1 (муладхара - безопасность, опора): [состояние и связь с запросом]
🟠 Чакра 2 (свадхистхана - эмоции, творчество): [состояние]
🟡 Чакра 3 (манипура - воля, самооценка): [состояние]
💚 Чакра 4 (анахата - любовь, раны): [состояние]
💙 Чакра 5 (вишудха - самовыражение): [состояние]
💜 Чакра 6 (аджна - интуиция): [состояние]
🤍 Чакра 7 (сахасрара - связь с Источником): [состояние]

Пиши кратко - 1 предложение на чакру.
"""

        response = self.client.chat.completions.create(
            model=self.model,
            messages=[{"role": "user", "content": prompt}],
            temperature=0.7,
            max_tokens=600
        )

        return response.choices[0].message.content, response.usage.total_tokens

    def _step4_transformation_phrases(self, request_text: str, step1_result: str, step2_result: str) -> tuple:
        """Шаг 4: Генерация трансформационных фраз"""

        prompt = f"""Ты — эксперт Мета-Метода. Создай трансформационные фразы для практики.

Запрос: "{request_text}"

Анализ программ:
{step1_result}

Родовые паттерны:
{step2_result}

Создай трансформационные фразы по классической формуле Мета-Метода:

1. ГЛАВНАЯ ФРАЗА (по формуле):
"Я признаю и даю место всем опытам в этой жизни, в моем роду и в моих прошлых жизнях, где [опиши конкретную проблему из запроса].

И даже если так было, и даже если это повторялось снова и снова, я прямо сейчас себя и нас за всё прощаю. Я люблю нас всех так, как Бог нас любит.

Я отдаю все долги с этим связанные и восстанавливаю все энерго-информационные балансы.

Я разрешаю убрать всё то, чем я сама держусь за эту программу.
Я разрешаю убрать всё то, что меня держит в этой программе.

Открываюсь новому, перерождаюсь и даю место новому."

2. ДОПОЛНИТЕЛЬНЫЕ АФФИРМАЦИИ (5 коротких фраз под конкретный запрос):
- "Я разрешаю себе..."
- "Я выбираю..."
- "Я достойна/достоин..."
- "Моя... [энергия/сила/любовь]..."
- "Я открываюсь..."
"""

        response = self.client.chat.completions.create(
            model=self.model,
            messages=[{"role": "user", "content": prompt}],
            temperature=0.8,
            max_tokens=800
        )

        return response.choices[0].message.content, response.usage.total_tokens

    def _step5_final_composition(self, username: str, request_text: str,
                                 step1: str, step2: str, step3: str, step4: str) -> tuple:
        """Шаг 5: Финальная компоновка всех частей в красивый формат"""

        prompt = f"""Ты — Мастер Мета-Метода. Создай ФИНАЛЬНЫЙ красивый разбор, объединив все части анализа.

Имя: {username}
Запрос: "{request_text}"

ЧАСТИ АНАЛИЗА:

1. Программы и уроки:
{step1}

2. Родовые паттерны:
{step2}

3. Чакры:
{step3}

4. Трансформационные фразы:
{step4}

Создай ФИНАЛЬНЫЙ ТЕКСТ в формате:

✨ **Сканер подсознания по Мета-Методу для {username}**

🔹 **Запрос:**
[точно повтори запрос]

**1. Контракты и подключки**
[из части 2, 2-3 предложения]

**2. Слои (откуда идёт программа)**
[из части 2, объедини родовое + кармическое]

**3. Поток энергии по центрам**
[вставь все 7 чакр с эмодзи из части 3]

**4. Главные программы, мешающие движению**
[из части 1, список в кавычках]

**5. Главные уроки души**
[из части 1]

**6. Родовые влияния**
[из части 2, развернуто]

**7. Связи из прошлых жизней**
[из части 2, если есть]

**8. Что важно изменить**
[из части 1, список]

**9. Трансформационные фразы**
[вставь ВСЕ фразы из части 4 - и главную формулу, и 5 аффирмаций]

**10. Рекомендация: следующий шаг**
Дай 2-3 практических совета на ближайшее время.

**11. Архетипический анализ** 🕊
Определи архетип (Творец/Целитель/Воин/Хранительница/Садовница души и т.п.). Опиши силу архетипа, текущий перекос и как вернуть баланс.

💫 **Вдохновляющее послание**
[2-3 предложения тёплого напутствия]

ВАЖНО:
- Пиши тёплым терапевтичным языком
- Используй эмодзи 🔮🌿✨💫🕊🌸
- Будь конкретной, не используй "возможно", "может быть"
- Текст должен вдохновлять и давать надежду
"""

        response = self.client.chat.completions.create(
            model=self.model,
            messages=[{"role": "user", "content": prompt}],
            temperature=0.8,
            max_tokens=2500
        )

        return response.choices[0].message.content, response.usage.total_tokens


# Функция для использования в боте
async def analyze_with_metamethod(request_text: str, username: str) -> tuple:
    """
    Главная функция для вызова из бота
    Возвращает (результат_анализа, использованные_токены)
    """
    analyzer = MetaMethodAnalyzer()
    result, total_tokens = analyzer.analyze(request_text, username)
    return result, total_tokens
